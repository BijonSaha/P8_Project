# ── InternVL2.5 image preprocessing ──────────────────────────────────────────
_INTERNVL_MEAN = (0.485, 0.456, 0.406)
_INTERNVL_STD  = (0.229, 0.224, 0.225)
_INTERNVL_SIZE = 448  # px; InternVL2.5 native tile resolution

def _internvl_load_image(path: Path) -> torch.Tensor:
    """Return [1, 3, 448, 448] float32 tensor for one image (single tile)."""
    transform = _tv_T.Compose([
        _tv_T.Lambda(lambda img: img.convert("RGB")),
        _tv_T.Resize((_INTERNVL_SIZE, _INTERNVL_SIZE), interpolation=_ILM.BICUBIC),
        _tv_T.ToTensor(),
        _tv_T.Normalize(mean=_INTERNVL_MEAN, std=_INTERNVL_STD),
    ])
    return transform(_PIL_Image.open(path)).unsqueeze(0)  # [1, 3, H, W]

