# ── MODEL LOADING ─────────────────────────────────────────────────────────────
def load_model(model_name: str, load_in_4bit: bool, max_gpu_memory_gib: float | None = None):
    global _MODEL_FAMILY
    mn = model_name.lower()
    is_gemma    = "gemma"    in mn
    is_internvl = "internvl" in mn
    is_llama_v  = "llama"    in mn and "vision" in mn

    if is_gemma:
        _MODEL_FAMILY = "gemma"
    elif is_internvl:
        _MODEL_FAMILY = "internvl"
    elif is_llama_v:
        _MODEL_FAMILY = "llama_vision"
    else:
        _MODEL_FAMILY = "qwen"

    print(f"\n  Loading {model_name} ...")
    print(f"  Family       : {_MODEL_FAMILY.upper()}")

    if load_in_4bit:
        print(f"  Quantization : INT4 bitsandbytes")
    else:
        print(f"  Quantization : bfloat16 (full precision)")

    if torch.cuda.is_available():
        props    = torch.cuda.get_device_properties(0)
        total_gb = props.total_memory / 1e9
        free_gb  = (props.total_memory - torch.cuda.memory_allocated()) / 1e9
        print(f"  GPU          : {torch.cuda.get_device_name(0)}")
        print(f"  VRAM total   : {total_gb:.1f} GB")
        print(f"  VRAM free    : {free_gb:.1f} GB")
        if free_gb < 5.0:
            raise SystemExit(
                f"[ERROR] Only {free_gb:.1f} GB VRAM free before model load — GPU is dirty.\n"
                "  → In Colab: Runtime → Disconnect and delete runtime → reconnect."
            )
    else:
        raise SystemExit("[ERROR] No CUDA GPU — switch to GPU runtime in Colab.")

    kwargs: dict = {"device_map": "auto"}
    if max_gpu_memory_gib is not None:
        kwargs["max_memory"] = {0: f"{max_gpu_memory_gib:.0f}GiB", "cpu": "80GiB"}
        print(f"  GPU cap      : {max_gpu_memory_gib:.0f} GiB  (overflow → CPU RAM)")
    if load_in_4bit:
        kwargs["quantization_config"] = BitsAndBytesConfig(
            load_in_4bit=True,
            bnb_4bit_quant_type="nf4",
            bnb_4bit_compute_dtype=torch.bfloat16,
            bnb_4bit_use_double_quant=True,
        )
    else:
        kwargs["torch_dtype"] = torch.bfloat16

    if is_internvl:
        # device_map="auto" is incompatible with InternVL2.5: its __init__ calls
        # .item() on linspace tensors before weights are materialized from meta device.
        # Load directly in BF16 and move to GPU manually instead.
        iv_kwargs: dict = {"trust_remote_code": True, "torch_dtype": torch.bfloat16}
        if "quantization_config" in kwargs:
            iv_kwargs["quantization_config"] = kwargs["quantization_config"]
        model = AutoModel.from_pretrained(model_name, **iv_kwargs).eval().cuda()
        processor = AutoTokenizer.from_pretrained(
            model_name, trust_remote_code=True)
    elif is_llama_v:
        model = MllamaForConditionalGeneration.from_pretrained(model_name, **kwargs)
        processor = AutoProcessor.from_pretrained(model_name)
    elif is_gemma:
        model = Gemma3ForConditionalGeneration.from_pretrained(model_name, **kwargs)
        processor = AutoProcessor.from_pretrained(model_name)
    else:  # qwen
        model = Qwen2_5_VLForConditionalGeneration.from_pretrained(model_name, **kwargs)
        processor = AutoProcessor.from_pretrained(model_name)

    used_gb = torch.cuda.memory_allocated() / 1e9
    print(f"  VRAM used after load: {used_gb:.1f} GB\n")
    return model, processor


# ── INFERENCE ─────────────────────────────────────────────────────────────────
def infer(model, processor, messages: list[dict]) -> str:

    if _MODEL_FAMILY == "internvl":
        # ── InternVL2.5 path ──────────────────────────────────────────────────
        # Extract system text and user content from standard message structure
        system_text = next(
            (m["content"] for m in messages if m["role"] == "system"
             and isinstance(m["content"], str)), "")
        user_msg = next(m for m in messages if m["role"] == "user")

        image_paths: list[Path] = []
        user_text = ""
        for c in user_msg["content"]:
            if c.get("type") == "image":
                image_paths.append(Path(c["image"]))
            elif c.get("type") == "text":
                user_text = c["text"]

        # Preprocess images → [N, 3, 448, 448] bfloat16 on GPU
        pv_list = [_internvl_load_image(p) for p in image_paths]
        pixel_values = torch.cat(pv_list, dim=0).to(model.device, dtype=torch.bfloat16)
        num_patches_list = [1] * len(image_paths)  # 1 tile per image (no dynamic tiling)

        # Build question: system instruction inline + image tokens + user prompt
        image_tokens = "<image>\n" * len(image_paths)
        question = (f"{system_text}\n\n" if system_text else "") + image_tokens + user_text

        gen_config = dict(max_new_tokens=MAX_NEW_TOKENS, do_sample=False)
        response = model.chat(
            processor, pixel_values, question, gen_config,
            num_patches_list=num_patches_list,
        )
        return response

    if _MODEL_FAMILY == "llama_vision":
        # ── Llama-3.2-Vision path ─────────────────────────────────────────────
        # Processor takes PIL images separately from the text messages.
        # Content blocks use {"type": "image"} with no path — images passed as list.
        llama_messages: list[dict] = []
        pil_images: list = []

        for m in messages:
            if m["role"] == "system":
                sys_text = m["content"] if isinstance(m["content"], str) else ""
                if sys_text:
                    llama_messages.append({"role": "system", "content": sys_text})
            elif m["role"] == "user":
                llama_content: list[dict] = []
                for c in m["content"]:
                    if c.get("type") == "image":
                        pil_images.append(_PIL_Image.open(c["image"]).convert("RGB"))
                        llama_content.append({"type": "image"})
                    elif c.get("type") == "text":
                        llama_content.append({"type": "text", "text": c["text"]})
                llama_messages.append({"role": "user", "content": llama_content})

        input_text = processor.apply_chat_template(
            llama_messages, add_generation_prompt=True)
        inputs = processor(
            images=pil_images, text=input_text, return_tensors="pt"
        ).to(model.device)
        with torch.no_grad():
            output_ids = model.generate(
                **inputs, max_new_tokens=MAX_NEW_TOKENS, do_sample=False)
        return processor.decode(
            output_ids[0][inputs["input_ids"].shape[-1]:], skip_special_tokens=True)

    if _MODEL_FAMILY == "gemma":
        # Gemma3 expects system content as a list of text blocks
        msgs = []
        for m in messages:
            if m["role"] == "system" and isinstance(m["content"], str):
                msgs.append({"role": "system", "content": [{"type": "text", "text": m["content"]}]})
            else:
                msgs.append(m)
        inputs = processor.apply_chat_template(
            msgs,
            add_generation_prompt=True,
            tokenize=True,
            return_dict=True,
            return_tensors="pt",
        ).to(model.device, dtype=torch.bfloat16)
        with torch.no_grad():
            output_ids = model.generate(
                **inputs,
                max_new_tokens=MAX_NEW_TOKENS,
                do_sample=TEMPERATURE > 0,
                temperature=TEMPERATURE if TEMPERATURE > 0 else 1.0,
            )
        return processor.decode(
            output_ids[0][inputs["input_ids"].shape[-1]:],
            skip_special_tokens=True,
        )

    # Qwen path
    from qwen_vl_utils import process_vision_info
    text = processor.apply_chat_template(
        messages, tokenize=False, add_generation_prompt=True
    )
    image_inputs, video_inputs = process_vision_info(messages)
    inputs = processor(
        text=[text],
        images=image_inputs,
        videos=video_inputs,
        padding=True,
        return_tensors="pt",
    ).to("cuda")
    with torch.no_grad():
        output_ids = model.generate(
            **inputs,
            max_new_tokens=MAX_NEW_TOKENS,
            do_sample=TEMPERATURE > 0,
            temperature=TEMPERATURE if TEMPERATURE > 0 else None,
            top_p=None,
        )
    trimmed = [out[len(inp):] for inp, out in zip(inputs.input_ids, output_ids)]
    return processor.batch_decode(trimmed, skip_special_tokens=True)[0]


# ── JSON PARSING ──────────────────────────────────────────────────────────────
def extract_json(raw: str) -> dict | None:
    """Parse model output — handles both structured CLASSIFICATION: format and JSON."""
    known  = set(ALL_GESTURES)
    _cmap  = {"high": 0.9, "medium": 0.65, "low": 0.35}

    # ── Primary: structured format (CLASSIFICATION: / CONFIDENCE: / RULING OUT:) ──
    # Allow optional angle brackets: model sometimes outputs <open_palm> vs open_palm
    cls_match  = re.search(r"CLASSIFICATION:\s*<?([a-zA-Z_]+)>?", raw)
    conf_match = re.search(r"CONFIDENCE:\s*<?(high|medium|low)>?", raw, re.IGNORECASE)
    if cls_match:
        gesture = cls_match.group(1).strip().lower()
        if gesture in known:
            conf_label = conf_match.group(1).lower() if conf_match else "medium"
            # Extract ruling-out line as reasoning
            ruling = re.search(r"Nearest alternative:.*?\n.*?Reason eliminated:\s*(.+)", raw, re.DOTALL)
            reasoning = ruling.group(1).strip()[:200] if ruling else ""
            return {
                "gesture":           gesture,
                "predicted_gesture": gesture,
                "confidence":        _cmap.get(conf_label, 0.65),
                "confidence_label":  conf_label,
                "reasoning":         reasoning,
            }

    # ── Fallback: JSON format ──
    cleaned = re.sub(r"```(?:json)?|```", "", raw).strip()
    s, e    = cleaned.find("{"), cleaned.rfind("}")
    if s == -1 or e <= s:
        return None
    try:
        parsed = json.loads(cleaned[s:e + 1])
    except json.JSONDecodeError:
        return None
    if "predicted_gesture" in parsed:
        parsed["gesture"] = parsed["predicted_gesture"]
    if "gesture" not in parsed:
        return None
    conf_raw = parsed.get("confidence", 0.0)
    if isinstance(conf_raw, str):
        parsed["confidence_label"] = conf_raw.lower().strip()
        parsed["confidence"]       = _cmap.get(conf_raw.lower().strip(), 0.5)
    else:
        try:
            conf = float(conf_raw)
            parsed["confidence"] = round(max(0.0, min(1.0, conf if conf <= 1 else conf / 100)), 4)
        except (TypeError, ValueError):
            parsed["confidence"] = 0.0
        parsed["confidence_label"] = ""
    parsed.setdefault("reasoning", "")
    return parsed


